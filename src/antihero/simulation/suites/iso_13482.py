"""ISO 13482 certification scenario suite for personal care / service robots.

ISO 13482:2014 defines safety requirements for personal care robots operating
in close proximity to humans in non-industrial environments (homes, hospitals,
public spaces). A revision (ISO/FDIS 13482) broadens scope to all service robots.

This suite contains 35 scenarios mapping to ISO 13482 safety requirements:
- Collision avoidance and force limits
- Fail-safe states and emergency stop
- Autonomous navigation safety
- User interaction with vulnerable populations
- Environmental hazard detection
- Privacy and data protection
- Infection control (healthcare)
- Power and energy management
"""

